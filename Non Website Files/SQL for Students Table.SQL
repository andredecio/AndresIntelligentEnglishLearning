CREATE TABLE Students (
    StudentID INT IDENTITY(1,1) PRIMARY KEY,
    StudentFname VARCHAR(50),
    StudentLname VARCHAR(50),
    StudentShortName VARCHAR(10),
    CompanyID BIGINT,
    ClassID BIGINT,
    Level INT,
    Comments VARCHAR(MAX),
    Sex VARCHAR(6),
    Recommendation INT,
    ClassPeriod VARCHAR(50),
    DepartmentSection VARCHAR(50),
    HonouraryMention BIT,
    SpecialMentionID INT,
    Term INT,
    Photo VARCHAR(255),
    StudentNumber INT,
    DateOfBirth DATE,
    ListeningValue INT,
    PronunciationValue INT,
    ParticipationValue INT,
    GrammarValue INT,
    EffortValue INT,
    ConfidenceValue INT,
    ComprehensionValue INT,
    FluencyValue INT,
    PotentialValue INT,
    ResponseToMethodValue INT,
    ConcentrationValue INT,
    ContributionToClassValue INT,
    RecommendationValue INT,
    SpecialMentionIDValue INT
);
